     <span>Algemene Voorwaarden – Zwerfreis</span>
    
    <span>Artikel 1 – Definities</span>
    <span>In deze voorwaarden wordt verstaan onder:</span>
    <span>Zwerfreis: de handelsnaam van het reisbureau dat reizen organiseert en aanbiedt.</span>
    <span>Reiziger: iedere persoon die gebruikmaakt van de diensten van Zwerfreis.</span>
    <span>Overeenkomst: de overeenkomst waarbij Zwerfreis zich verbindt tot het leveren van een of meerdere reisdiensten aan de reiziger.</span>
    
    <span>Artikel 2 – Totstandkoming van de overeenkomst</span>
    <span>1. De overeenkomst komt tot stand zodra de reiziger een reis boekt via de website, telefonisch of per e-mail.</span>
    <span>2. De reiziger ontvangt een bevestiging per e-mail met de boekingsgegevens. Deze bevestiging geldt als bewijs van de overeenkomst.</span>
    
    <span>Artikel 3 – Betaling</span>
    <span>1. Bij boeking dient een aanbetaling van 20% van de reissom te worden voldaan.</span>
    <span>2. Het resterende bedrag dient uiterlijk 6 weken voor vertrek te zijn voldaan.</span>
    <span>3. Bij boeking binnen 6 weken voor vertrek dient het volledige bedrag direct te worden voldaan.</span>
    
    <span>Artikel 4 – Annulering door de reiziger</span>
    <span>1. Annulering dient schriftelijk of per e-mail te gebeuren.</span>
    <span>2. Bij annulering is de reiziger de volgende kosten verschuldigd:</span>
    <span>- tot 42 dagen voor vertrek: 20% van de reissom</span>
    <span>- 42 tot 21 dagen voor vertrek: 50% van de reissom</span>
    <span>- 21 tot 7 dagen voor vertrek: 75% van de reissom</span>
    <span>- binnen 7 dagen voor vertrek: 100% van de reissom</span>
    
    <span>Artikel 5 – Wijziging door Zwerfreis</span>
    <span>1. Zwerfreis behoudt zich het recht voor om de reis te wijzigen wegens zwaarwegende omstandigheden (zoals overmacht, natuurrampen of onvoorziene wijzigingen).</span>
    <span>2. De reiziger wordt hierover zo spoedig mogelijk geïnformeerd en ontvangt indien mogelijk een passend alternatief of recht op teruggave van (een deel van) de reissom.</span>
    
    <span>Artikel 6 – Aansprakelijkheid</span>
    <span>1. Zwerfreis is niet aansprakelijk voor schade die voortvloeit uit:</span>
    <span>- overmachtssituaties;</span>
    <span>- persoonlijke ongevallen;</span>
    <span>- vertragingen of wijzigingen in vervoer;</span>
    <span>- verlies of diefstal van bagage.</span>
    <span>2. De aansprakelijkheid van Zwerfreis is in elk geval beperkt tot maximaal de reissom.</span>
    
    <span>Artikel 7 – Verzekeringen</span>
    <span>1. De reiziger is zelf verantwoordelijk voor het afsluiten van een reis- en/of annuleringsverzekering.</span>
    <span>2. Zwerfreis adviseert ten zeerste om dit af te sluiten bij boeking.</span>
    
    <span>Artikel 8 – Paspoort, visa en vaccinaties</span>
    <span>1. De reiziger is zelf verantwoordelijk voor het bezit van geldige reisdocumenten, visa en eventuele vaccinaties.</span>
    <span>2. Zwerfreis is niet aansprakelijk voor de gevolgen van het niet voldoen aan deze verplichtingen.</span>
    
    <span>Artikel 9 – Klachten</span>
    <span>1. Klachten tijdens de reis dienen zo spoedig mogelijk gemeld te worden bij de reisleiding of via het noodnummer van Zwerfreis.</span>
    <span>2. Klachten na afloop dienen uiterlijk binnen 14 dagen na terugkomst schriftelijk te worden ingediend.</span>
    
    <span>Artikel 10 – Toepasselijk recht</span>
    <span>Op alle overeenkomsten met Zwerfreis is het Nederlands recht van toepassing. Eventuele geschillen worden voorgelegd aan de bevoegde rechter in Nederland.</span>

